package calculatorpractice;

class Calculator {

	int a;
	int b;

	void add() {

		int c = a + b;
		System.out.println(c);

	}

	void sub() {

		int d = a - b;

		System.out.println(d);

	}

	void div() {

		int e = a / b;
		System.out.println(e);
	}

	void mul() {

		int g = a * b;
		System.out.println(g);
	}

}
