package returntypemethods;

class Calculatorr {

	int adding() {
		int a = 10;
		int b = 12;
		int t = a + b;
		return t;
	}

	int add(int a, int b) {

		int t = a + b;
		return t;

	}

	int multi() {
		int c = 13;
		int d = 15;
		int f = c * d;
		return f;
	}

	int multiplying(int d, int f) {
		int e = f * d;
		return e;
	}

	int div() {
		int g = 11;
		int h = 12;
		int f = g / h;
		return f;

	}

	int division(int d, int e) {

		int f = d / e;
		return f;
	}

	int sub() {

		int e = 20;
		int f = 30;
		int g = e - f;
		return g;
	}

	int subtraction(int e, int f) {
		int g = e - f;
		return g;
	}

}
